def mean_obliquity(jd_tdb):
    """Compute the mean obliquity of the ecliptic."""

    # Compute time in Julian centuries from epoch J2000.0.

    t = (jd_tdb - T0) / 36525.0;

    # Compute the mean obliquity in arcseconds.  Use expression from the
    # reference's eq. (39) with obliquity at J2000.0 taken from eq. (37)
    # or Table 8.

    epsilon = (((( -  0.0000000434   * t
                   -  0.000000576  ) * t
                   +  0.00200340   ) * t
                   -  0.0001831    ) * t
                   - 46.836769     ) * t + 84381.406;

    return epsilon

def equation_of_the_equinoxes_complimentary_terms(jd_tt):
    """Compute the complementary terms of the equation of the equinoxes.

    `jd_tt` - numpy.array of dates for which to compute the terms

    """

    # Interval between fundamental epoch J2000.0 and current date.

    t = (jd_tt - T0) / 36525.0

    # Fundamental Arguments.

    fa = zeros((14, jd_tt.shape[0]))

    # Mean Anomaly of the Moon.

    fa[0] = ((485868.249036 +
              (715923.2178 +
              (    31.8792 +
              (     0.051635 +
              (    -0.00024470)
              * t) * t) * t) * t) * ASEC2RAD
              + fmod(1325.0*t, 1.0) * tau) % tau;

    # Mean Anomaly of the Sun.

    fa[1] = ((1287104.793048 +
              (1292581.0481 +
              (     -0.5532 +
              (     +0.000136 +
              (     -0.00001149)
              * t) * t) * t) * t) * ASEC2RAD
              + fmod (99.0*t, 1.0) * tau) % tau;

    # Mean Longitude of the Moon minus Mean Longitude of the Ascending
    # Node of the Moon.

    fa[2] = (( 335779.526232 +
              ( 295262.8478 +
              (    -12.7512 +
              (     -0.001037 +
              (      0.00000417)
              * t) * t) * t) * t) * ASEC2RAD
              + fmod (1342.0*t, 1.0) * tau) % tau;

    # Mean Elongation of the Moon from the Sun.

    fa[3] = ((1072260.703692 +
              (1105601.2090 +
              (     -6.3706 +
              (      0.006593 +
              (     -0.00003169)
              * t) * t) * t) * t) * ASEC2RAD
              + fmod (1236.0*t, 1.0) * tau) % tau;

    # Mean Longitude of the Ascending Node of the Moon.

    fa[4] = (( 450160.398036 +
              (-482890.5431 +
              (      7.4722 +
              (      0.007702 +
              (     -0.00005939)
              * t) * t) * t) * t) * ASEC2RAD
              + fmod (-5.0*t, 1.0) * tau) % tau;

    fa[ 5] = (4.402608842 + 2608.7903141574 * t) % tau
    fa[ 6] = (3.176146697 + 1021.3285546211 * t) % tau
    fa[ 7] = (1.753470314 +  628.3075849991 * t) % tau
    fa[ 8] = (6.203480913 +  334.0612426700 * t) % tau
    fa[ 9] = (0.599546497 +   52.9690962641 * t) % tau
    fa[10] = (0.874016757 +   21.3299104960 * t) % tau
    fa[11] = (5.481293872 +    7.4781598567 * t) % tau
    fa[12] = (5.311886287 +    3.8133035638 * t) % tau
    fa[13] = (0.024381750 +    0.00000538691 * t) * t

    # Evaluate the complementary terms.

    s0 = 0.0
    s1 = 0.0

    for i in reversed(range(33)):

        a = 0.0

        for j in range(14):
            a += ke0_t[i][j] * fa[j]

        s0 += se0_t[i][0] * sin(a) + se0_t[i][1] * cos(a)

    a = 0.0

    for j in range(14):
        a += ke1[j] * fa[j]

    s1 += se1[0] * sin(a) + se1[1] * cos(a)
    c_terms = s0 + s1 * t
    c_terms *= ASEC2RAD
    return c_terms

def iau2000a(jd_tt):

    # Interval between fundamental epoch J2000.0 and given date.

    t = (jd_tt - T0) / 36525.0

    # Compute fundamental arguments from Simon et al. (1994), in radians.

    a = fundamental_arguments(t)

    # ** Luni-solar nutation **
    # Summation of luni-solar nutation series (in reverse order).

    arg = fmod(nals_t.dot(a), tau)

    sarg = sin(arg)
    carg = cos(arg)

    stsc = array((sarg, t * sarg, carg)).T
    ctcs = array((carg, t * carg, sarg)).T

    dp = tensordot(stsc, lunisolar_longitude_coefficients)
    de = tensordot(ctcs, lunisolar_obliquity_coefficients)

    # Convert from 0.1 microarcsec units to radians.

    factor = 1.0e-7 * ASEC2RAD
    dpsils = dp * factor
    depsls = de * factor

    # ** Planetary nutation. **

    # Mean anomaly of the Moon.

    al = fmod(2.35555598 + 8328.6914269554 * t, tau)

    # Mean anomaly of the Sun.

    alsu = fmod(6.24006013 + 628.301955 * t, tau)

    # Mean argument of the latitude of the Moon.

    af = fmod(1.627905234 + 8433.466158131 * t, tau)

    # Mean elongation of the Moon from the Sun.

    ad = fmod(5.198466741 + 7771.3771468121 * t, tau)

    # Mean longitude of the ascending node of the Moon.

    aom = fmod(2.18243920 - 33.757045 * t, tau)

    # General accumulated precession in longitude.

    apa = (0.02438175 + 0.00000538691 * t) * t

    # Planetary longitudes, Mercury through Neptune (Souchay et al. 1999).

    alme = fmod(4.402608842 + 2608.7903141574 * t, tau)
    alve = fmod(3.176146697 + 1021.3285546211 * t, tau)
    alea = fmod(1.753470314 +  628.3075849991 * t, tau)
    alma = fmod(6.203480913 +  334.0612426700 * t, tau)
    alju = fmod(0.599546497 +   52.9690962641 * t, tau)
    alsa = fmod(0.874016757 +   21.3299104960 * t, tau)
    alur = fmod(5.481293871 +    7.4781598567 * t, tau)
    alne = fmod(5.321159000 +    3.8127774000 * t, tau)

    # Summation of planetary nutation series (in reverse order).

    a = array((al, alsu, af, ad, aom,
               alme, alve, alea, alma, alju, alsa, alur, alne, apa))

    arg = fmod(napl_t.dot(a), tau)
    sc = array((sin(arg), cos(arg))).T

    dpsipl = tensordot(sc, nutation_coefficients_longitude) * factor
    depspl = tensordot(sc, nutation_coefficients_obliquity) * factor

    # Total: Add planetary and luni-solar components.

    dpsi = dpsipl + dpsils
    deps = depspl + depsls

    return dpsi, deps

